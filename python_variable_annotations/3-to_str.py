#!/usr/bin/env python3
"""
The to_str function converts a float to a string and returns it.
"""


def to_str(n: float) -> str:
    """
    Args:
    n (float): The float to convert to a string.

    Returns:
    str: The string representation of the float.

    Example:
    >>> to_str(3.14)
    '3.14'
    """
    return str(n)
